<h2>Este es LegoBox 3</h2>
<p>Estamos utilizando el modulo por default : <span class="labelp label-info">Lego</span></p>
<p>Este modulo solo se utiliza como ejemplo para experimentar cada nueva caracteristica en el entorno del <b>LegoBox FrameWork</b>.</p>

<p>Componentes principales:</p>
<ul>
	<li><b>Core :</b> Quien carga, controla y optimiza las transiciones entre modulos y el nucleo el general.</li>
	<li><b>Module :</b> Operaciones con modulos.</li>
	<li><b>View :</b> Operaciones con las vistas de cada modulo.</li>
</ul>
